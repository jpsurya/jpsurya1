package Day5;

public class EvenOdd {
	public static void main(String[] args) {
		int num = 847;
		int num2 = 432;
		if (num % 2 == 0) {
			System.out.println("even");

		} else {
			System.out.println("odd");

		}
		if (num2 % 2 != 0) {
			System.out.println("odd");

		} else {
			System.out.println("even");
		}

	}

}
